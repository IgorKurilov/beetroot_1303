import os

def print_directory_tree(startpath, indent=''):
    """
    –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –¥—Ä—É–∫—É—î –¥–µ—Ä–µ–≤–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ–π —Ç–∞ —Ñ–∞–π–ª—ñ–≤ –ø–æ—á–∏–Ω–∞—é—á–∏ –∑ –≤–∫–∞–∑–∞–Ω–æ—ó –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó.
    
    Parameters:
    startpath (str): –ü–æ—á–∞—Ç–∫–æ–≤–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—è, –∑ —è–∫–æ—ó –ø–æ—á–∏–Ω–∞—î—Ç—å—Å—è –ø–æ–±—É–¥–æ–≤–∞ –¥–µ—Ä–µ–≤–∞.
    indent (str): –í—ñ–¥—Å—Ç—É–ø –¥–ª—è –≤–∫–ª–∞–¥–µ–Ω–∏—Ö –µ–ª–µ–º–µ–Ω—Ç—ñ–≤. –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î—Ç—å—Å—è –¥–ª—è –≤—ñ–∑—É–∞–ª—ñ–∑–∞—Ü—ñ—ó —Ä—ñ–≤–Ω—ñ–≤ –¥–µ—Ä–µ–≤–∞.
    """
    # –ü—Ä–æ—Ö–æ–¥–∏–º–æ –ø–æ –≤—Å—ñ—Ö –µ–ª–µ–º–µ–Ω—Ç–∞—Ö –≤ –ø–æ—Ç–æ—á–Ω—ñ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó
    for item in os.listdir(startpath):
        # –§–æ—Ä–º—É—î–º–æ –ø–æ–≤–Ω–∏–π —à–ª—è—Ö –¥–æ –µ–ª–µ–º–µ–Ω—Ç–∞
        path = os.path.join(startpath, item)
        # –Ø–∫—â–æ –µ–ª–µ–º–µ–Ω—Ç —î –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—î—é, –¥—Ä—É–∫—É—î–º–æ –π–æ–≥–æ –∑ —ñ–∫–æ–Ω–∫–æ—é üìÅ —Ç–∞ –≤–∏–∫–ª–∏–∫–∞—î–º–æ —Ñ—É–Ω–∫—Ü—ñ—é —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ
        if os.path.isdir(path):
            # –Ø–∫—â–æ –µ–ª–µ–º–µ–Ω—Ç —î –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—î—é (os.path.isdir(path)), –¥—Ä—É–∫—É—î–º–æ –π–æ–≥–æ –∑ —ñ–∫–æ–Ω–∫–æ—é üìÅ.
            print(f"{indent}üìÅ {item}") 
            print_directory_tree(path, indent + '    ')
        # –Ø–∫—â–æ –µ–ª–µ–º–µ–Ω—Ç —î —Ñ–∞–π–ª–æ–º, –¥—Ä—É–∫—É—î–º–æ –π–æ–≥–æ –∑ —ñ–∫–æ–Ω–∫–æ—é üìÑ
        else:
            print(f"{indent}üìÑ {item}")

